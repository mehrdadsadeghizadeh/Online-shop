// Set the price gap
let priceGap = 450000;

// Adding event listners to price input elements
const priceInputvalue = document.querySelectorAll(".price-input input");
for (let i = 0; i < priceInputvalue.length; i++) {
  priceInputvalue[i].addEventListener("input", (e) => {
    // Parse min and max values of the range input
    let minp = parseInt(priceInputvalue[0].value);
    let maxp = parseInt(priceInputvalue[1].value);
    let diff = maxp - minp;
    if (minp < 200000) {
      alert("قیمت کمترین محصول ما از 200,000 تومان شروع میشود!");
      priceInputvalue[0].value = 200000;
      minp = 200000;
    }

    // Validate the input values
    if (maxp > 1500000) {
      alert("قیمت بیشترین محصول ما تا 1,500,000 تومان میباشد!");
      priceInputvalue[1].value = 1500000;
      maxp = 1500000;
    }

    if (minp > maxp - priceGap) {
      priceInputvalue[0].value = maxp - priceGap;
      minp = maxp - priceGap;

      if (minp < 0) {
        priceInputvalue[0].value = 0;
        minp = 0;
      }
    }

    // Check if the price gap is met
    // and max price is within the range
    if (diff >= priceGap && maxp <= rangeInputvalue[1].max) {
      if (e.target.className === "min-input") {
        rangeInputvalue[0].value = minp;
        let value1 = rangeInputvalue[0].max;
        rangevalue.style.left = `${(minp / value1) * 100}%`;
      } else {
        rangeInputvalue[1].value = maxp;
        let value2 = rangeInputvalue[1].max;
        rangevalue.style.right = `${100 - (maxp / value2) * 100}%`;
      }
    }
  });
  // Add event listeners to range input elements
  for (let i = 0; i < rangeInputvalue.length; i++) {
    rangeInputvalue[i].addEventListener("input", (e) => {
      let minVal = parseInt(rangeInputvalue[0].value);
      let maxVal = parseInt(rangeInputvalue[1].value);

      let diff = maxVal - minVal;

      // Check if the price gap is exceeded
      if (diff < priceGap) {
        // Check if the input is the min range input
        if (e.target.className === "min-range") {
          rangeInputvalue[0].value = maxVal - priceGap;
        } else {
          rangeInputvalue[1].value = minVal + priceGap;
        }
      } else {
        // Update price inputs and range progress
        priceInputvalue[0].value = minVal;
        priceInputvalue[1].value = maxVal;
        let rangevalueStyleLeft = `${(minVal / rangeInputvalue[0].max) * 100}%`;
        let rangevalueStyleRight = `${
          100 - (maxVal / rangeInputvalue[1].max) * 100
        }%`;
        localStorage.setItem(
          "rangevalueStyleLeft",
          JSON.stringify(rangevalueStyleLeft)
        );
        let localrangevalueStyleLeft = JSON.parse(
          localStorage.getItem("rangevalueStyleLeft")
        );
        rangevalue.style.left = localrangevalueStyleLeft;

        localStorage.setItem(
          "rangevalueStyleRight",
          JSON.stringify(rangevalueStyleRight)
        );
        let localrangevalueStyleRight = JSON.parse(
          localStorage.getItem("rangevalueStyleRight")
        );
        rangevalue.style.right = localrangevalueStyleRight;
      }
    });
  }
}
